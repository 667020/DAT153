package com.hvl.no.dat153_obllig1_quizzy;

public class GalleryItem {
    private String name;
    private int image;

    public GalleryItem(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public java.lang.String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }
}
